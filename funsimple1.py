def PowerA3(x):
    return x**3
A = float(input("A = "))
B = float(input("B = "))
C = float(input("C = "))
D = int(input("D = "))
E = int(input("E = "))

print("A = ", PowerA3(A))
print("B = ", PowerA3(B))
print("C = ", PowerA3(C))
print("D = ", PowerA3(D))
print("E = ", PowerA3(E))
